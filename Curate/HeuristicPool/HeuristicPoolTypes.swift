// MARK: - Scored Playlist

/// A playlist scored against a parsed vibe
struct ScoredPlaylist {
    let playlist: ProviderPlaylist
    let moodScore: Float       // 0-1 based on keyword matches
    let genreScore: Float      // 0-1 based on genre matches
    let editorialBonus: Float  // 0.2 if editorial, 0 otherwise
    let totalScore: Float      // Combined weighted score

    /// Description for debugging
    var debugDescription: String {
        "[\(String(format: "%.2f", totalScore))] \(playlist.name) " +
        "(mood: \(String(format: "%.2f", moodScore)), " +
        "genre: \(String(format: "%.2f", genreScore)), " +
        "editorial: \(editorialBonus > 0 ? "yes" : "no"))"
    }
}

