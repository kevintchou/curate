// MARK: - Feature Flag

/// Feature flag for recommendation engine selection
enum RecommendationEngineFlag {
    private static let key = "recommendationEngine"

    static var current: RecommendationEngine {
        get {
            let raw = UserDefaults.standard.string(forKey: key) ?? ""
            return RecommendationEngine(rawValue: raw) ?? .toolCalling
        }
        set {
            UserDefaults.standard.set(newValue.rawValue, forKey: key)
        }
    }

    static var isHeuristicPoolEnabled: Bool {
        current == .heuristicPool
    }

    static var isHybridPoolEnabled: Bool {
        current == .hybridPool
    }

    static var isArtistSeedsEnabled: Bool {
        current == .artistSeeds
    }

    static var isToolCallingEnabled: Bool {
        current == .toolCalling
    }
}

// MARK: - Keyword Mappings (Loaded from JSON)

/// Mood keyword mappings loaded from MoodKeywords.json
struct MoodKeywordMappings: Codable {
    let mappings: [String: [String]]

    /// Get keywords for a mood category
    func keywords(for mood: MoodCategory) -> [String] {
        mappings[mood.rawValue] ?? []
    }

    /// Find mood categories that match any of the given tokens
    func matchingMoods(for tokens: [String]) -> [(MoodCategory, Int)] {
        var results: [(MoodCategory, Int)] = []

        for mood in MoodCategory.allCases {
            let keywords = self.keywords(for: mood)
            let matchCount = tokens.filter { token in
                keywords.contains { keyword in
                    token.contains(keyword) || keyword.contains(token)
                }
            }.count

            if matchCount > 0 {
                results.append((mood, matchCount))
            }
        }

        return results.sorted { $0.1 > $1.1 }
    }

    /// Load from bundled JSON
    static func loadFromBundle() -> MoodKeywordMappings {
        guard let url = Bundle.main.url(forResource: "MoodKeywords", withExtension: "json"),
              let data = try? Data(contentsOf: url),
              let mappings = try? JSONDecoder().decode([String: [String]].self, from: data) else {
            print("⚠️ Failed to load MoodKeywords.json, using defaults")
            return MoodKeywordMappings(mappings: defaultMoodKeywords)
        }
        return MoodKeywordMappings(mappings: mappings)
