/// Genre synonym mappings for normalization
struct GenreSynonymMappings: Codable {
    let mappings: [String: [String]]

    /// Normalize a genre string to its canonical form
    func normalize(_ genre: String) -> String {
        let lowercased = genre.lowercased()

        for (canonical, synonyms) in mappings {
            if canonical.lowercased() == lowercased {
                return canonical
            }
            if synonyms.contains(where: { $0.lowercased() == lowercased }) {
                return canonical
            }
        }

        return genre
    }

    /// Check if a text contains any genre or its synonyms
    func containsGenre(_ genre: String, in text: String) -> Bool {
        let lowercasedText = text.lowercased()
        let lowercasedGenre = genre.lowercased()

        // Direct match
        if lowercasedText.contains(lowercasedGenre) {
            return true
        }

        // Synonym match
        if let synonyms = mappings[genre] ?? mappings.first(where: { $0.key.lowercased() == lowercasedGenre })?.value {
            return synonyms.contains { lowercasedText.contains($0.lowercased()) }
        }

        return false
    }

    /// Load from bundled JSON
    static func loadFromBundle() -> GenreSynonymMappings {
        guard let url = Bundle.main.url(forResource: "GenreSynonyms", withExtension: "json"),
              let data = try? Data(contentsOf: url),
              let mappings = try? JSONDecoder().decode([String: [String]].self, from: data) else {
            print("⚠️ Failed to load GenreSynonyms.json, using defaults")
            return GenreSynonymMappings(mappings: defaultGenreSynonyms)
        }
        return GenreSynonymMappings(mappings: mappings)
    }

