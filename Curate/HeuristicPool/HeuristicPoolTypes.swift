// MARK: - Parsed Vibe

/// Output from the VibeParser - represents the parsed intent from user input
struct ParsedVibe: Equatable {
    let rawInput: String
    let moods: [MoodCategory]
    let activity: ActivityContext?
    let timeContext: TimeContext?
    let confidence: Float  // 0-1, for fallback decisions

    /// Canonical intent string for caching (hash of normalized components)
    var canonicalIntent: String {
        var components: [String] = []

        // Add sorted moods for consistency
        components.append(contentsOf: moods.map { $0.rawValue }.sorted())

        if let activity = activity {
            components.append("activity:\(activity.rawValue)")
        }

        if let time = timeContext {
            components.append("time:\(time.rawValue)")
        }

        let combined = components.joined(separator: "-")
        return combined.isEmpty ? "generic-vibe" : combined
    }

    /// Hash for pool lookups
    var intentHash: String {
        canonicalIntent.sha256Hash
    }

    /// Check if this is a low-confidence parse that might need fallback
    var needsFallback: Bool {
        confidence < 0.4 || moods.isEmpty
    }

    // MARK: - Factory Methods

    static func empty(input: String) -> ParsedVibe {
        ParsedVibe(
            rawInput: input,
            moods: [],
            activity: nil,
            timeContext: nil,
            confidence: 0.0
        )
    }
}

